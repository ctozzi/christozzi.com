<p>I am an historian, researcher and writer. Trained as an historian of revolutionary France, I received a Ph.D. in 2013 from Johns Hopkins. My first book, <i><a href="http://www.upress.virginia.edu/title/4921">Nationalizing France's Army: Foreign, Black, and Jewish Troops in the French Military, 1715-1831</a></i> (University of Virginia Press, 2016), explores the history of nationality, citizenship and nation-building during the French revolutionary era from the perspective of troops who served in the French army but were not considered fully French.</p>

<p>I am also interested in the history and culture of technology. My second book, <i><a href="https://mitpress.mit.edu/books/fun-and-profit">For Fun and Profit: A History of the Free and Open Source Software Revolution</a></i> (MIT Press, 2017), explores the history of software whose source code is freely shared, as well as the ideological, economic and cultural impact of such software. For more on this project, please click <a href="foss_history">here</a>.</p>

<p>I currently teach in the Science and Technology Studies department at Rensselaer Polytechnic Institute.</p>

<p>Beyond my scholarly work, I write frequently on a freelance basis for a variety of media sites on topics such as open source, machine learning and blockchain.</p>

